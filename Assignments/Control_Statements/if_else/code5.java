class code5 {
    public static void main(String[] args) {
        float floatNum1 = 120f;

        if (floatNum1 % 6 == 0) {
            System.out.println(floatNum1 + " is divisible by 6.");
        } else {
            System.out.println(floatNum1 + " is not divisible by 6.");
        }

        float floatNum2 = 89.96f;

        if (floatNum2 % 6 == 0) {
            System.out.println(floatNum2 + " is divisible by 6.");
        } else {
            System.out.println(floatNum2 + " is not divisible by 6.");
        }
    }
}

