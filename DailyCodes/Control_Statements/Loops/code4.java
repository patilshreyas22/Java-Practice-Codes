class code4{


	public static void main(String args []){

		System.out.println("In For Loop");
	
		for(int i=1 ; i<=5 ;){
		
			System.out.println("Hello World");

			i++;
		
		}

		System.out.println("In While Loop");

		int i = 1;
		while(i<=5){
		
			System.out.println("Hello World");
			i++;
		
		}
	
	
	}


}
